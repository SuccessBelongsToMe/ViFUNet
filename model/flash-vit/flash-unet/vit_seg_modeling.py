class FLASHEncoder(nn.Module):
    def __init__(
        self,
        config,
        depth,
        group_size = 256,
        query_key_dim = 128,
        expansion_factor = 2.,
        causal = False,
        attn_dropout = 0.,
        norm_type = 'scalenorm',
        shift_tokens = True
    ):
        super().__init__()
        assert norm_type in ('scalenorm', 'layernorm'), 'norm_type must be one of scalenorm or layernorm'
        if norm_type == 'scalenorm':
            norm_klass = ScaleNorm
        elif norm_type == 'layernorm':
            norm_klass = nn.LayerNorm
        # self.token_emb = PatchEmbedding(in_channels=in_channel,patch_size=path_size,emb_size=emb_size,img_size=img_size)
        # # self.abs_pos_emb = ScaledSinuEmbedding(dim)
        # self.group_size = group_size
        # rotary_pos_emb = RotaryEmbedding(dim = min(32, query_key_dim))
        # max rotary embedding dimensions of 32, partial Rotary embeddings, from Wang et al - GPT-J
        self.layers = nn.ModuleList([FLASH(dim = config.hidden_size, group_size = group_size, query_key_dim = query_key_dim, expansion_factor = expansion_factor, causal = causal, dropout = attn_dropout, rotary_pos_emb = None, norm_klass = norm_klass, shift_tokens = shift_tokens) for _ in range(depth)])
        self.encoder_norm = LayerNorm(config.hidden_size, eps=1e-6)
        # self.to_logits = nn.Sequential(
        #     nn.LayerNorm(dim),
        #     Rearrange('b c (n h w) -> b c n h w',n=128,h=path_size,w=path_size),
